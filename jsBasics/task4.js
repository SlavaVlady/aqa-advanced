// 4.1 Площа кола
const radiusCircle = 5;
const areaCircle = Math.PI * Math.pow(radiusCircle, 2);
console.log('Circle area:', areaCircle.toFixed(2));

// 4.2 Площа прямокутника
const lengthRectangle = 10;
const widthRectangle = 4;
const areaRectangle = lengthRectangle * widthRectangle;
console.log('Rectangle area:', areaRectangle.toFixed(2));

// 4.3 Об'єм циліндра
const radiusCylinder = 3;
const heightCylinder = 7;
const volumeCylinder = Math.PI * Math.pow(radiusCylinder, 2) * heightCylinder;
console.log('Cylinder volume:', volumeCylinder.toFixed(2));
